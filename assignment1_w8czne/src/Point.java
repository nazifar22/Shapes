/**
 * Package that contains the classes of the geometric shapes.
 */

/**
 * The point that is used throughout the shapes.
 */
public class Point {
    double x;
    double y;

    /**
     * Instantiates the new Point object when called.
     * @param x
     * @param y
     */
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
